package io.github.nahkd123.voxelwrench.support.fabric.client.gui;

public abstract class AbstractWidget implements Widget {
	protected boolean visible = true;
	protected int x = 0, y = 0, width = 1, height = 1;
	protected Renderers renderers;

	@Override
	public boolean isVisible() {
		return visible;
	}

	@Override
	public void setVisible(boolean visible) {
		this.visible = visible;
	}

	@Override
	public int getX() {
		return x;
	}

	@Override
	public void setX(int x) {
		this.x = x;
	}

	@Override
	public int getY() {
		return y;
	}

	@Override
	public void setY(int y) {
		this.y = y;
	}

	@Override
	public int getWidth() {
		return width;
	}

	@Override
	public void setWidth(int width) {
		this.width = width;
	}

	@Override
	public int getHeight() {
		return height;
	}

	@Override
	public void setHeight(int height) {
		this.height = height;
	}

	@Override
	public void useRenderers(Renderers renderers) {
		// TODO null check: use default renderer
		this.renderers = renderers;
	}

	@Override
	public Renderers getRenderers() {
		return renderers;
	}
}
